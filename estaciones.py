# Estaciones del año ❄️🌸☀️🍂

numero = int(input("¿En qué mes estamos? 0-12: "))

if (numero == 1 or numero == 2 or numero == 3):
    print("Es invierno ❄️")

elif (numero == 4 or numero == 5 or numero == 6):
    print("Es primavera 🌸")

elif (numero == 7 or numero == 8 or numero == 9):
    print("Es verano ☀️")

elif (numero == 10 or numero == 11 or numero == 12):
    print("Es otoño 🍂")

else:
    print("Invalid")
